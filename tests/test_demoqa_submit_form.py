    browser.all('.table>tbody>tr')[5].should(have.exact_text('Subjects Maths, Computer Science, Economics'))
    browser.all('.table>tbody>tr')[6].should(have.exact_text('Hobbies Reading, Music'))
    browser.all('.table>tbody>tr')[7].should(have.exact_text('Picture test.png'))
    browser.all('.table>tbody>tr')[8].should(have.exact_text('Address Odesskaya, bld. 24, appt. 23'))
